3. FAO Global Forest Resources Assessment (FRA):
  
  Site: https://oceancolor.gsfc.nasa.gov/data/use-data/
  Descrição: 
  - Avaliação global dos recursos florestais realizada pela Organização das Nações Unidas para a Alimentação e a Agricultura.
- A Avaliação Global dos Recursos Florestais (FRA) da FAO fornece informações essenciais para a compreensão da extensão dos recursos florestais, sua condição, gestão e usos.
-A avaliação em curso, FRA 2025, examina a situação e as tendências de mais de 60 variáveis relacionadas com as florestas em 236 países e territórios no período 1990-2025. 
- Os dados FRA 2025 são recolhidos utilizando termos e definições comummente acordados através de um processo de comunicação transparente e rastreável e de uma rede bem estabelecida de correspondentes nacionais oficialmente nomeados que abrange 191 países e territórios.

#Dados robusto#

